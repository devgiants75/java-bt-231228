package c_Chapter02;

// 조건문 (Condition)
// : 프로그램의 흐름을 제어하는 데 사용되는 기본적인 구조 중 하나

// 자바의 조건문 종류
// : if, else, else if, switch 문
public class A_If {

	public static void main(String[] args) {
//		if문
//		: 조건에 따라 실행시키고자 하는 로직(프로세스)이 다를 때 사용하는 구문
//		: 조건이 true 일 때 실행할 코드를 지정하는 구문

//		if문 기본 구조
//		if (조건-'새라면') {
//			: 조건이 true일 때 실행할 코드
//			: '날 수 있다.'
//		}
		
//		: 조건은 true / false의 결과를 반환하는 boolean 형태의 표현식
		
		int age = 20;
		
		if (age > 19) {
//			중괄호 안의 코드 블럭은 들여쓰기가 기본
//			조건이 true일 경우에만 {} 안에 작성한 코드 블럭이 실행
			System.out.println("성인입니다.");
		}
		
		
		
		
	}

}
